var showGDElement = function (elem) {
	elem.style.display = 'block';
};

// Hide an element
var hideGDElement = function (elem) {
	elem.style.display = 'none';
};

function goodDollarDonation(){
    var GDThankYou = document.getElementById('good_dollar_donation_thank_you');
    var GDFalse = document.getElementById('good_dollar_donation_false');
    if (typeof web3 !== 'undefined' ) {
    // Use Mist/MetaMask's provider
    //var web3js = new Web3(web3.currentProvider);
    
    var goodDollarContract = web3.eth.contract(goodDollarABI).at(goodDollarTokenAddress);
    var decimals = web3.toBigNumber(goodDollarDecimals);
    var amountDonation = web3.toBigNumber(goodDollarAmountDonation);
    var value = amountDonation.times(web3.toBigNumber(10).pow(decimals));
    
    goodDollarContract.transfer(goodDollarToAddress, value, (error, txHash) => {
        // it returns tx hash because sending tx
        if(txHash){
            var gdTransactionDetails = document.getElementById('good_dollar_transaction_details');
            gdTransactionDetails.href="https://rinkeby.etherscan.io/tx/"+txHash;
            showGDElement(GDThankYou);
        }else{
            showGDElement(GDFalse);
        }
      });
  } else {
    showGDElement(GDFalse);
  }
}

